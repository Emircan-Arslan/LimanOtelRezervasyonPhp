    <td colspan="3"><p>HAMAM VE SAĞLIK</p>
      <p><strong>Bedensel ve Ruhsal Arınma Geleneği<br />
    </strong>Buhar banyosunun faydaları yıllar öncesinden biliniyordu. Bugün, buhar banyoları cildi ve bedeni temizlemek (arındırma) ve toksinden arındırmak, kan dolaşımını arttırmak, immun sistemi uyarmak ve komple bir fiziksel ve mental zindeliği  desteklemek  için gerekli bir metod olarak düşünülmüştür.Buhar banyoları kişiyi gevşetir ve stresi azaltır. Kas ağrıları ya da artritle görülen sıkıntılar, sıcağın kasları gevşetmesi, ağrı ve inflamasyonu azaltmasıyla rahatlama meydana gelir. Astım ve alerjik sıkıntısı olan kişiler, sıcağın akciğerlerin hava yolunu genişletmesi ile nefes alış verişi kolaylaşır. Sıcak, yaygın soğuk algınlığını tedavi etmez fakat tıkanıklığı azaltır ve hızlı toparlanmaya yardımcı olur.  Buhar banyoları, deri kan akımını arttırdığı ve terlemeyi oluşturduğu için cildinize iyi gelir. Yetişkin bir kişi hamamda, ortalama bir saatte yaklaşık 1 lt ter ya da su kaybeder. İyi bir terleme gözeneklerden kir ve yüzeydeki ölü deri tabakasını çıkarır ve cilde sağlıklı bir parlaklık kazandırır.. Akut sıvı kaybı, toplam beden suyu ağırlığında bir azalmaya neden olur, fakat bu geçici bir durum olduğu için tekrar sıvı alımıyla birlikte çabucak tam miktarını geri alacaktır. Sıcak, bir çok kültürde iyileştirmek için uzun süre kullanılan bir terapi ajanıdır. Bedenimizin tüm fonksiyonları kimyasal reaksiyonlara bağlıdır ve kimyasal reaksiyonlar sıcaktan direk etkilenirler. Bu durum, dokularımız ve sağlımız üzerinde bir etki oluşturmaktan ileri gelir.</p>
      <p><strong>Hamam, sağlık açısından aşağıdaki durumlara iyi geldiği bilinmektedir.</strong></p>
      <ul>
        <li>Stresi hafifletir, gevşetir ve dinlendirir</li>
        <li>Kas gerginliklerini ve ağrılarını giderir ve kısıtlı eklemleri acar</li>
        <li>İmmun sistemi destekler</li>
        <li>Lenf sistemi temizliğini artırır.</li>
        <li>Kan dolaşımını arttırır.</li>
        <li>Bedenin metabolik aktivitesini.</li>
        <li>Sığuk, astım ya da alerjik durumlardan dolayı oluşan sinus tılanıklınlarını azaltır.</li>
        <li>Cildin genç ve taze kalmasını sağlar.</li>
      </ul>      <ul>
        <li>Buhar sıcağı son zamanlarda kanser ve enfeksiyon hastalıklarına karşı kullanılan terapilerden birisi buhar sıcağı ve buhar banyolarıdır.</li>
        <li>Buhar banyoları bedeni yağ-depo toksinlerden arındırmak için çok etkili.</li>
        <li>Terleme sırasında buhar etkili bir biçimde toksinleri derinin yüzeyinden temizler.</li>
        <li> Buhar uygulaması vaskuler akımı iyileştirir, hücresel seviye</li>
        </ul>
