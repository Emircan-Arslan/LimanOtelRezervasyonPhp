      <p>• Sporcular egzersize gösterdikleri uyum sonucu terlediklerinde fazla tuz kaybetmezler; sulu terlerler. Bu sayede vücuttan kurşun, nikel, cıva gibi zararlı toksinler atılmış olur. Sauna, insanın pasif durumda iken sulu terlemesini sağlar. Böylece aktif terlemede oluşan yorgunluğun tam tersine saunada terleme ile yorgunluk giderilir. <br />
        • Düzenli sauna banyoları solunum yollarının daha düzenli çalışmasına da yardımcı olmaktadır. <br />
        • Sauna banyosu kardio-vaskular sistemini ve kan basıncını olumlu yönde etkileyerek, kalbe ve bütün kardio-vaskular sistemine stressiz bir antrenman yaptırmaktadır. <br />
        • Sauna, toplam kan proteinlerinde artışa neden olur. Bu artış da dokulara daha fazla oksijenin ulaşmasını sağlar. <br />
        • Sauna, vücudun dış etkilere karşı direncini artırır. <br />
        • Düzenli sauna banyoları kasları gevşetmek suretiyle vücuttaki romatizma ve kireçlenme ağrılarına iyi gelir.<br />
    • Soğuk duşla tamamlanmış ve kurallarına uygun olarak yapılan sauna ziyaretlerinin yararları görüldüğü gibi çok geniş bir yelpazeye sahiptir. Sauna ile günlük yaşamın bütün stresinden ve yorgunluğundan kurtulur, yaşamdan keyif alırsınız. </p>
