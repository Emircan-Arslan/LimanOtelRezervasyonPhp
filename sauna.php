      <p><strong>SAUNA ve SAĞLIĞINIZA FAYDALARI</strong>:</p>
      <p>• Sauna; kalp, dolaşım ve sinir sistemini düzenleyen bir kültür-fizik olayıdır. İnsan vücudundaki hızlı, yoğun sıcaklık artışları ve düşüşleri saunanın ana prensibini oluşturmaktadır. <br />
        • Deri tabakasının üst düzeyinde sıcaklık normalden 10 °C daha fazla yükselir, aynı zamanda deri tabakasının altında sıcaklık normalden 1°C daha artar. Bu da vücuttaki hastalıklara karşı koyan madde olan antikor üretimini artırır. <br />
        • Kan damarları genişlediği için kan akışı hızlanır. Organizmamız vücut sıcaklığını deriyi soğutarak sabit tutmaya çalışır ve yoğun bir terleme ile reaksiyon verir. Deri, ölü hücrelerden temizlenerek canlanır. Yumuşak ve pürüzsüz bir yüzeye sahip olur. <br />
        • Yoğun terleme, insan vücudundaki su ve toksinlerin dengesini ayarlar. Özellikle spor yaptıktan sonra saunaya girilmesi tavsiye edilmektedir. Bunun sebebi olarak da kas ağrılarına neden olan laktik asidin terleme yoluyla atılması gösterilmektedir. Finli atletlerin mukavemet sporlarındaki başarısının saunadan kaynaklandığı düşünülmektedir. Günümüzde düzenli sauna banyoları başarılı atletlerin çalışma programlarında yer almaktadır. <br />
        • Sauna ile metabolizmanın çalışması hızlanır ve terleme için enerji gerekeceğinden bir sauna seansında yaklaşık 300 kalori yakılmış olur . Bu miktar 3-4 km koşmakla aynı değerdedir.</p>
